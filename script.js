//on search form submit
$("#search-form").submit(function (e) {
  e.preventDefault();
  var query = $("#search-form input").val().toLowerCase();

  $(".product").hide();
  $(".product").each(function () {
    var title = $(this).data("make").toLowerCase(),
      country_code = $(this).data("model").toLowerCase(),
      explanation_1 = $(this).data("type").toLowerCase();

    if (
      title.indexOf(query) > -1 ||
      country_code.indexOf(query) > -1 ||
      explanation_1.indexOf(query) > -1
    ) {
      $(this).show();
    }
  });
});
